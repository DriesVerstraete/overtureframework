            end do
            end do
            end do
          end if
        else
          ! stencil optimized routines
          call getcuu_sixth3DOLD( cc,dx,dy,dz,dt,cuu )
          call getcuv_sixth3DOLD( cc,dx,dy,dz,dt,cuv )
          call getcvu_sixth3DOLD( cc,dx,dy,dz,dt,cvu )
          call getcvv_sixth3DOLD( cc,dx,dy,dz,dt,cvv )
